import UIKit

//Swift是一门开发iOS、macOS、watchOS和tvOS应用的新语言
//Swift包含了C和Objective-C上所有基础数据类型 Int表示整型；Double和Float表示浮点型;Bool是布尔值；String是文本型数据 Swift还提供了三个基本的集合类型: Array、Set和Dictionary
//就像C语言一样 Swift使用变量来进行存储并通过变量名来关联值。 在Swift中， 广泛地使用着值不可变的变量，它们就是常量， 而且比C语言的常量更强大。在Swift中，如果你要处理的值不需要改变，那使用常量可以让你的代码更加安全并且更清晰的表达意图。
//Swift还增加了Objective-C中没有的高阶数据类型，比如元组。元组可以让你创建或者传递一组数据，比如作为函数的返回值时，可以用一个元组可以返回多个值
//Swift还增加了可选(Optional)类型 用于处理值缺失的情况。可选表示“那儿有一个值，并且它等于x”或者"那儿没有值"。可选有点像在Objective-C中使用nil，但是它可以用在任何类型上，不仅仅是类。可选类型比Objective-C中的nil指针更加安全也更具表现力。
//Swift是一门类型安全的语言 这意味着Swift可以让你清楚的知道值的类型。如果你的代码期望得到一个String类型，类型安全会阻止你传入一个Int。类型安全可以帮助你在开发阶段尽早发现并修改错误

